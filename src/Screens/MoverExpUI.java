    private void jMoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMoverActionPerformed
        try {
        Expediente exp = administrador.buscarPorID(idExpediente);

        if (exp == null) {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ el expediente con ID: " + idExpediente);
            return;
        }

        String anteriorDep = exp.getDependenciaActual();
        String nuevaDep = (String) boxDependencias.getSelectedItem();

        String movimiento = "Movido de " + anteriorDep + " a " + nuevaDep;
        administrador.registrarMovimiento(exp, movimiento);

        exp.setDependenciaActual(nuevaDep);
        
        JOptionPane.showMessageDialog(this, "Expediente movido a " + nuevaDep);
        showUsersUI.refreshTable();
        ScreenManager.goBack(this);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al mover expediente: " + e.getMessage());
    }
    }//GEN-LAST:event_jMoverActionPerformed

